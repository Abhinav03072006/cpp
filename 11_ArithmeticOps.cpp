#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 3;
    cout << "Addition: " << a + b << endl;
    cout << "Subtraction: " << a - b << endl;
    cout << "Multiplication: " << a * b << endl;
    cout << "Division (Quotient): " << a / b << endl;
    cout << "Modulus (Remainder): " << a % b << endl;
    return 0;
}